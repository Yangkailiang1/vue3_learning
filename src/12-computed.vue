<script setup>
	import { ref, computed} from 'vue'
	// 商品列表(原始数据)
	const goodsList = ref([
	{ id: 1, name: '篮球', num: 1 },
	{ id: 2, name: '玩具', num: 3 },
	{ id: 3, name: '书籍', num: 2 }
	])

  const totalNum = computed(() => {
    return goodsList.value.reduce((prev, item) => prev + item.num, 0)
  })
</script>
<template>
	<h3>⽐特⼈的礼物清单</h3>
	<table>
		<tr>
			<th>名字</th>
			<th>数量</th>
		</tr>
		<tr v-for="item in goodsList" :key="item.id">
			<td>{{ item.name }}</td>
			<td>{{ item.num }}个</td>
		</tr>
	</table>
	<!-- ⽬标：统计求和, 展⽰礼物总数 -->
	<p>礼物总数：{{totalNum}}个</p>
</template>
